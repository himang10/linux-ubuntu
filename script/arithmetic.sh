#!/bin/bash
#
# Bash 연산자 실습 스크립트
#

###############################################
# 기본 산술 연산 (+, -, *, /)
###############################################

a=10
b=3

echo "=== 기본 산술 연산 ==="
echo "a = $a, b = $b"

echo "더하기 (a + b)        = $((a + b))"
echo "빼기   (a - b)        = $((a - b))"
echo "곱하기 (a * b)        = $((a * b))"
echo "나누기 (a / b)        = $((a / b))"  # 정수 나눗셈

echo


###############################################
# 거듭제곱(**)
###############################################

echo "=== 거듭제곱 (** 연산자) ==="
echo "a^b = $((a ** b))"    # 10^3 = 1000
echo

###############################################
# ++
###############################################
x=10

(( x++ ))      # x=11
echo "$x"


###############################################
# 나머지(%)
###############################################

echo "=== 나머지 (% 연산자) ==="
echo "a % b = $((a % b))"    # 10 % 3 = 1
echo


###############################################
# 복합 대입 연산자 (+=, -=, *=, /=, %=)
###############################################

echo "=== 복합 대입 연산자 ==="
x=10
echo "초기 x = $x"

((x+=5))
echo "x+=5   → $x"

((x-=3))
echo "x-=3   → $x"

((x*=2))
echo "x*=2   → $x"

((x/=4))
echo "x/=4   → $x"

((x%=3))
echo "x%=3   → $x"
echo


###############################################
# for
###############################################
echo "=== for test ==="

declare -i count=0
echo "루프에서 count 증가시키기"

for i in 1 2 3 4 5; do
    count+=1
done

echo "최종 count 값 = $count"
echo

# for 루프
for (( i=0; i<5; i++ )); do
    echo "i = $i"
done
echo

###############################################
# if
###############################################
# 조건으로 사용
echo "=== if test ==="
if (( x > 10 )); then
    echo "(( )): x는 10보다 큽니다"
fi
echo
###############################################
# while
###############################################
echo "=== while test ==="
(( i=0 ))
while (( i < 5 )); do
    echo "i = $i"
    (( i++ ))
done
echo

echo "연산자 실습 완료!"

