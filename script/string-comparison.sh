#!/bin/bash
#
# string-comparison.sh
# Bash 문자열 비교 실습 예제
#

str1="apple"
str2="banana"
#empty=""

echo "=== 문자열 비교 실습 시작 ==="
echo "str1 = $str1"
echo "str2 = $str2"
echo


##########################################################
# [[ ]] 문자열 같음 / 다름
##########################################################
echo "== [[ ]] 문자열 비교 =="
[[ "$str1" == "$str2" ]] && echo "[[ ]] 같음" || echo "[[ ]] 다름"
[[ "$str1" != "$str2" ]] && echo "[[ ]] '$str1' != '$str2'"
echo


##########################################################
# [[ ]] 사전순 비교 (<, >)
# ASCII 순서 apple < banana
##########################################################
echo "== [[ ]] 사전순 비교 =="
[[ "$str1" < "$str2" ]] && echo "[[ ]] '$str1' < '$str2'"
[[ "$str1" > "$str2" ]] && echo "[[ ]] '$str1' > '$str2'" || echo "[[ ]] '$str1' <= '$str2'"
echo


##########################################################
# [ ] 문자열 같음/다름 (POSIX)
##########################################################
echo "== [ ] 문자열 비교 =="
[ "$str1" = "$str2" ] && echo "[ ] 같음" || echo "[ ] 다름"
[ "$str1" != "$str2" ] && echo "[ ] '$str1' != '$str2'"
echo


##########################################################
# [[ ]] 빈 문자열 검사 (-z / -n)
##########################################################
echo "== [[ ]] 빈 문자열 검사 =="
[[ -z "$empty" ]] && echo "[[ ]] empty 문자열: -z true"
[[ -n "$str1" ]] && echo "[[ ]] '$str1' 은 비어 있지 않음 (-n)"
echo


echo "=== 문자열 비교 실습 종료 ==="

